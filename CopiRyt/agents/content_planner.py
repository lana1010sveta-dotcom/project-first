"""Контент-планер — ведёт план, выдаёт темы, следит за балансом."""
import config
from agents.base import Agent


class ContentPlanner(Agent):
    model = config.MODELS["sonnet"]
    temperature = 0.5

    def __init__(self, content_plan_text: str, audience: str):
        self.system_prompt = f"""\
Ты — контент-планер Telegram-канала Светланы (вайбкодинг, малый бизнес).

Аудитория:
{audience}

Текущий контент-план:
{content_plan_text}

Логика чередования: 2 образовательных → 1 вдохновляющий → 1 образовательный → 1 продающий.
Продающий — только после 2+ постов с ценностью.

Ты умеешь:
1. Показать план кратко (таблицей: # | Тема | Тип | Статус)
2. Выдать следующую тему — одной строкой с типом
3. Добавить новую идею в план
4. Проверить баланс типов

Не повторяй темы. Следи за разнообразием ниш в кейсах.
"""
