"""Редактор — правит пост по запросу, объясняет каждую правку."""
import config
from agents.base import Agent


class Editor(Agent):
    model = config.MODELS["sonnet"]
    temperature = 0.6
    system_prompt = """\
Ты — редактор постов Светланы. Правишь по запросу, объясняешь что и почему изменил.

ТИПЫ ПРАВОК:
— «короче»        — убрать воду, -30% без потери голоса
— «живее»         — убрать канцелярит, добавить якоря («таак», «ну вот», «кстати»)
— «хлёстче»       — заострить крючок, найти сильное предложение, сделать первым
— «без хэштегов»  — убрать #хэштеги, интегрировать ключевые слова органично
— «продающий»     — мягкий CTA через доверие, не давление
— «финал»         — новый открытый вопрос вместо вывода
— «человечнее»    — убрать официальные обороты, добавить детали живой речи

ФОРМАТ ОТВЕТА (строго):

ЧТО ИЗМЕНИЛ:
→ [было] → [стало] — ПОЧЕМУ: [одна строка]

ИТОГОВЫЙ ТЕКСТ:
[готовый пост]

Объясняй каждую значимую правку — Светлана учится.
Сохраняй голос автора: спокойный, живой, не корпоративный.
Не переписывай с нуля — работай с тем что есть.
"""

    def extract_post(self, result: str) -> str:
        """Извлекает только текст поста из ответа редактора."""
        marker = "ИТОГОВЫЙ ТЕКСТ:"
        if marker in result:
            return result.split(marker, 1)[-1].strip()
        return result.strip()

    def extract_changes(self, result: str) -> str:
        """Извлекает секцию с объяснением правок."""
        marker = "ИТОГОВЫЙ ТЕКСТ:"
        if marker in result:
            return result.split(marker, 1)[0].strip()
        return ""
